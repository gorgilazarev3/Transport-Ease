  bool validateFields(BuildContext context) {
    bool result = false;

    if (_model.providerTypeValue == null || _model.providerTypeValue!.isEmpty) {
      Fluttertoast.showToast(msg: "Мора да изберете тип на превозник!");
    } else {
      if (_model.providerTypeValue!.toLowerCase() == "превоз на патници") {
        if (_model.transportingTypeValue == null ||
            _model.transportingTypeValue!.isEmpty) {
          Fluttertoast.showToast(
              msg:
                  "Мора да изберете или меѓуградски или интернационален превозник!");
        } else if (_model.taxiSeatsController.text.isEmpty) {
          Fluttertoast.showToast(
              msg:
                  "Мора да го внесете бројот на достапни седишта за патници во возилото!");
        } else {
          result = true;
        }
      } else if (_model.providerTypeValue!.toLowerCase() ==
          "превоз на пакети") {
        if (_model.carrierCapacityController.text.isEmpty) {
          Fluttertoast.showToast(
              msg:
                  "Мора да го внесете капацитетот на возилото за пренос на пакети!");
        } else {
          result = true;
        }
      }
    }
    return result;
  }

  void saveProviderInfo(BuildContext context) {
    if (validateFields(context)) {
      String userId = FirebaseAuth.instance.currentUser!.uid;
      Map carInfoMap = {};
      if (_model.providerTypeValue!.toLowerCase() == "превоз на патници") {
        String routes_type = "local";
        if (_model.transportingTypeValue!.toLowerCase() == "меѓуградски") {
          routes_type = "local";
        } else {
          routes_type = "international";
        }

        carInfoMap = {
          "provider_type": "passengers_provider",
          "provider_seats": _model.taxiSeatsController.text,
          "routes_type": routes_type,
        };
      } else {
        carInfoMap = {
          "provider_type": "carrier_provider",
          "carrier_capacity": _model.carrierCapacityController.text,
        };
      }

      providersRef.child(userId).child("provider_details").set(carInfoMap);
      context.go("/home");
    }
